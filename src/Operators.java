public class Operators {
    public static void  operators(){
        int a = 10;
        int b = 23;
        int result;
        a += 2;
        b += 3;
        result = a++;
        System.out.println(result);
        result = ++a;
        System.out.println(result);
        result = a--;
        System.out.println(result);
        result = --a;
        System.out.println(result);
        result = a + b;
        System.out.println(result);
        result = a - b;
        System.out.println(result);
        result = b * a;
        System.out.println(result);
        result = b / a;
        System.out.println(result);
        result = b % a;
        System.out.println(result);
        result *= 2;
        System.out.println(result);
        result /= 2;
        System.out.println(result);
        result %= 3;
        System.out.println(result);
    }
}
